# –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç–∞, —Ä–∞–≤–Ω–æ–≥–æ \dfrac{n!}{k! (n-k)!}


# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
from math import factorial


def compute_binom(n, k):
    binom = factorial(n) / (factorial(k) * factorial(n - k))
    return int(binom)


# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
